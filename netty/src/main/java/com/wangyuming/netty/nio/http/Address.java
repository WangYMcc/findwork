package com.wangyuming.netty.nio.http;

public class Address {
    private String stress1;

    private String stress2;

    private String city;

    private String state;

    private String postCode;

    private String country;

    public String getStress1() {
        return stress1;
    }

    public void setStress1(String stress1) {
        this.stress1 = stress1;
    }

    public String getStress2() {
        return stress2;
    }

    public void setStress2(String stress2) {
        this.stress2 = stress2;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}
